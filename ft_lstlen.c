#include "libft.h"

size_t	ft_lstlen(t_list *lst)
{
	int	i;

	i = 0;
	if (!lst)
		return (i);
	while (!(lst == NULL))
	{
		i++;
		lst = lst->next;
	}
	return (i);
}
